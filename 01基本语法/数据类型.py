# 组合类型 列表, 元组, 字典, 集合
# list 列表
list = [1, 2, 3, 4, 5]
print(list[1])

# tuple 元组,元素不支持修改
tuple = (1, 2, 3, 4, 5)

# dict 字典 {key1: value1, key2, value2, ...}
student = {202001: "张三", 202002: "李四", 202003: "王五"}
print(student[202002])

# set 集合 一系列互不相等的集合,无序的
student_set = {"张三", "李四", "王五", "张三"}
print(student_set)